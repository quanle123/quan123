public class Mua {
    private String MaPN;
    private String MSHH;
    private int SoLuong;
    private float DonGiaNhap;

    /**
     * @return the MaPN
     */
    public String getMaPN() {
        return MaPN;
    }

    /**
     * @param MaPN the MaPN to set
     */
    public void setMaPN(String MaPN) {
        this.MaPN = MaPN;
    }

    /**
     * @return the MSHH
     */
    public String getMSHH() {
        return MSHH;
    }

    /**
     * @param MSHH the MSHH to set
     */
    public void setMSHH(String MSHH) {
        this.MSHH = MSHH;
    }

    /**
     * @return the SoLuong
     */
    public int getSoLuong() {
        return SoLuong;
    }

    /**
     * @param SoLuong the SoLuong to set
     */
    public void setSoLuong(int SoLuong) {
        this.SoLuong = SoLuong;
    }

    /**
     * @return the DonGiaNhap
     */
    public float getDonGiaNhap() {
        return DonGiaNhap;
    }

    /**
     * @param DonGiaNhap the DonGiaNhap to set
     */
    public void setDonGiaNhap(float DonGiaNhap) {
        this.DonGiaNhap = DonGiaNhap;
    }
    
    
}
